function fselPrePrintStats( maDataTrain, maDataTest, veWeight, veFeature)

%  
% fselPrePrintStats( maDataTrain, maDataTest, veWeight, veFeature);
% 
% prints statistics about input data: 
% # features before and after selection, # observations for train and test
%
% Input:    maDataTrain     : train data
%           maDataTest      : test data
%           veWeight        : ranking vector
%           veFeature       : name of features
%
% Output:   None
%
% (c) 20070510 Holger Harms, Wearable Computing Lab., ETH Zurich
%

% get dimensions of input train data
[obsTr feats] = size(maDataTrain);
% get dimensions of input train data
[obsTe feats] = size(maDataTest);

fprintf('[PrePrintStats] Input features:. %d\n', feats);
fprintf('[PrePrintStats] Training data:.. %d\n', obsTr);
fprintf('[PrePrintStats] Test data:...... %d\n\n', obsTe);


